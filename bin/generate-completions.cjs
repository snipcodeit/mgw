// ---------------------------------------------------------------------------
// Bash generator
// ---------------------------------------------------------------------------

function generateBash() {
  const subcommandNames = COMMANDS.map(c => c.name).join(' ');

  const globalFlagNames = GLOBAL_FLAGS
    .map(f => f.flag.split(',').map(s => s.trim().split(' ')[0]).join(' '))
    .join(' ');

  const caseBlocks = COMMANDS.map(cmd => {
    const cmdFlags = [...cmd.flags, ...GLOBAL_FLAGS]
      .map(f => f.flag.split(',').map(s => s.trim().split(' ')[0]).join(' '))
      .join(' ');
    return `        ${cmd.name})\n          COMPREPLY=( $(compgen -W "${cmdFlags}" -- "$cur") )\n          return\n          ;;`;
  }).join('\n');

  const innerFlagCases = COMMANDS.map(cmd => {
    const cmdFlags = [...cmd.flags, ...GLOBAL_FLAGS]
      .map(f => f.flag.split(',').map(s => s.trim().split(' ')[0]).join(' '))
      .join(' ');
    return `      ${cmd.name}) COMPREPLY=( $(compgen -W "${cmdFlags}" -- "$cur") ) ;;`;
  }).join('\n');

  return `#!/usr/bin/env bash
# mgw bash completion script
# Source this file or add to /etc/bash_completion.d/mgw
# Generated by bin/generate-completions.cjs

_mgw() {
  local cur prev words cword
  _init_completion 2>/dev/null || {
    COMPREPLY=()
    cur="${COMP_WORDS[COMP_CWORD]}"
    prev="${COMP_WORDS[COMP_CWORD-1]}"
  }

  local subcommands="${subcommandNames}"
  local global_flags="${globalFlagNames}"

  case "$prev" in
    mgw)
      COMPREPLY=( $(compgen -W "$subcommands $global_flags" -- "$cur") )
      return
      ;;
${caseBlocks}
    --model)
      COMPREPLY=( $(compgen -W "claude-opus-4-5 claude-sonnet-4-5 claude-haiku-4-5" -- "$cur") )
      return
      ;;
    --state)
      COMPREPLY=( $(compgen -W "open closed all" -- "$cur") )
      return
      ;;
    --assignee)
      COMPREPLY=( $(compgen -W "all @me" -- "$cur") )
      return
      ;;
  esac

  if [[ "$cur" == -* ]]; then
    local subcmd=""
    for word in "\${words[@]}"; do
      if [[ " $subcommands " == *" $word "* ]]; then
        subcmd="$word"
        break
      fi
    done

    case "$subcmd" in
${innerFlagCases}
      *) COMPREPLY=( $(compgen -W "$global_flags" -- "$cur") ) ;;
    esac
    return
  fi

  COMPREPLY=( $(compgen -W "$subcommands" -- "$cur") )
}

complete -F _mgw mgw
`;
}

// ---------------------------------------------------------------------------
// Zsh generator
// ---------------------------------------------------------------------------

function generateZsh() {
  const globalArgStrings = GLOBAL_FLAGS.map(f => {
    const parts = f.flag.split(',').map(s => s.trim());
    const long = parts.find(p => p.startsWith('--'));
    const short = parts.find(p => p.startsWith('-') && !p.startsWith('--'));
    const takesArg = f.flag.includes('<');
    const longBase = long ? long.split(' ')[0] : null;
    const shortBase = short ? short.split(' ')[0] : null;

    let spec;
    if (short && longBase) {
      spec = `'(${shortBase} ${longBase})'{${shortBase},${longBase}}'[${f.desc}]'`;
    } else if (longBase) {
      spec = `'${longBase}[${f.desc}]'`;
    } else {
      spec = `'${shortBase}[${f.desc}]'`;
    }

    if (takesArg && longBase === '--model') {
      spec = spec.replace("]'", "]:model:(claude-opus-4-5 claude-sonnet-4-5 claude-haiku-4-5)'");
    } else if (takesArg) {
      spec = spec.replace("]'", "]:value:'");
    }

    return `    ${spec}`;
  }).join('\n');

  const subcommandDescs = COMMANDS
    .map(c => `    '${c.name}:${c.desc}'`)
    .join('\n');

  const subcommandCases = COMMANDS.map(cmd => {
    const allFlags = [...cmd.flags, ...GLOBAL_FLAGS];
    const flagLines = allFlags.map(f => {
      const parts = f.flag.split(',').map(s => s.trim());
      const long = parts.find(p => p.startsWith('--'));
      const short = parts.find(p => p.startsWith('-') && !p.startsWith('--'));
      const takesArg = f.flag.includes('<');
      const longBase = long ? long.split(' ')[0] : null;
      const shortBase = short ? short.split(' ')[0] : null;

      let spec;
      if (short && longBase) {
        spec = `'(${shortBase} ${longBase})'{${shortBase},${longBase}}'[${f.desc}]'`;
      } else if (longBase) {
        spec = `'${longBase}[${f.desc}]'`;
      } else {
        spec = `'${shortBase}[${f.desc}]'`;
      }

      if (f.choices) {
        spec = spec.replace("]'", `]:value:(${f.choices.join(' ')})'`);
      } else if (takesArg) {
        spec = spec.replace("]'", "]:value:'");
      }

      return `            ${spec}`;
    }).join(' \\\n');

    const argLines = cmd.args.map((a, i) => `            '${i + 1}:${a}:'`).join(' \\\n');

    const allLines = [flagLines, argLines].filter(Boolean).join(' \\\n');

    return `        ${cmd.name})\n          _arguments \\\n${allLines} \\\n            && return 0\n          ;;`;
  }).join('\n');

  return `#compdef mgw
# mgw zsh completion script
# Place in a directory on your $fpath (e.g. /usr/local/share/zsh/site-functions/_mgw)
# Generated by bin/generate-completions.cjs

_mgw() {
  local context state state_descr line
  typeset -A opt_args

  local -a global_flags
  global_flags=(
${globalArgStrings}
  )

  _arguments -C \\
    "\${global_flags[@]}" \\
    '(-h --help)'{-h,--help}'[display help]' \\
    '(-V --version)'{-V,--version}'[output the version number]' \\
    '1: :_mgw_subcommands' \\
    '*:: :->subcommand_args' \\
    && return 0

  case "$state" in
    subcommand_args)
      case "$words[1]" in
${subcommandCases}
      esac
      ;;
  esac
}

_mgw_subcommands() {
  local -a subcommands
  subcommands=(
${subcommandDescs}
  )
  _describe 'mgw subcommand' subcommands
}

_mgw_issue_numbers() {
  local -a active_issues
  local active_dir=".mgw/active"
  if [[ -d "$active_dir" ]]; then
    for f in "$active_dir"/*.json; do
      [[ -f "$f" ]] || continue
      local num="\${\${f:t}%%[-_]*}"
      [[ "$num" =~ ^[0-9]+$ ]] && active_issues+=("$num")
    done
  fi
  if (( \${#active_issues[@]} > 0 )); then
    _describe 'issue number' active_issues
  else
    _message 'issue number'
  fi
}

_mgw "$@"
`;
}

// ---------------------------------------------------------------------------
// Fish generator
// ---------------------------------------------------------------------------

function generateFish() {
  const lines = [
    '# mgw fish completion script',
    '# Place in ~/.config/fish/completions/mgw.fish',
    '# Generated by bin/generate-completions.cjs',
    '',
    '# Disable file completions by default for mgw',
    'complete -c mgw -f',
    '',
    '# ---------------------------------------------------------------------------',
    '# Global flags (available before subcommand)',
    '# ---------------------------------------------------------------------------',
  ];

  for (const f of GLOBAL_FLAGS) {
    const parts = f.flag.split(',').map(s => s.trim());
    const long = parts.find(p => p.startsWith('--'));
    const short = parts.find(p => p.startsWith('-') && !p.startsWith('--'));
    const longBase = long ? long.split(' ')[0].replace(/^--/, '') : null;
    const shortBase = short ? short.split(' ')[0].replace(/^-/, '') : null;
    const takesArg = f.flag.includes('<');

    let line = `complete -c mgw -n '__fish_use_subcommand'`;
    if (shortBase) line += ` -s ${shortBase}`;
    if (longBase)  line += ` -l ${longBase}`;
    line += ` -d '${f.desc}'`;
    if (takesArg)  line += ' -r';
    lines.push(line);
  }

  lines.push('');
  lines.push('# ---------------------------------------------------------------------------');
  lines.push('# Subcommands');
  lines.push('# ---------------------------------------------------------------------------');

  for (const cmd of COMMANDS) {
    lines.push(`complete -c mgw -n '__fish_use_subcommand' -a ${cmd.name} -d '${cmd.desc}'`);
  }

  for (const cmd of COMMANDS) {
    if (cmd.flags.length === 0) continue;

    lines.push('');
    lines.push(`# ---------------------------------------------------------------------------`);
    lines.push(`# ${cmd.name}`);
    lines.push(`# ---------------------------------------------------------------------------`);

    for (const f of cmd.flags) {
      const parts = f.flag.split(',').map(s => s.trim());
      const long = parts.find(p => p.startsWith('--'));
      const short = parts.find(p => p.startsWith('-') && !p.startsWith('--'));
      const longBase = long ? long.split(' ')[0].replace(/^--/, '') : null;
      const shortBase = short ? short.split(' ')[0].replace(/^-/, '') : null;
      const takesArg = f.flag.includes('<');

      let line = `complete -c mgw -n '__fish_seen_subcommand_from ${cmd.name}'`;
      if (shortBase) line += ` -s ${shortBase}`;
      if (longBase)  line += ` -l ${longBase}`;
      line += ` -d '${f.desc}'`;
      if (takesArg) {
        line += ' -r';
        if (f.choices) {
          line += ` -a '${f.choices.join(' ')}'`;
        }
      }
      lines.push(line);
    }

    // Also add global flags for this subcommand
    for (const f of GLOBAL_FLAGS) {
      const parts = f.flag.split(',').map(s => s.trim());
      const long = parts.find(p => p.startsWith('--'));
      const short = parts.find(p => p.startsWith('-') && !p.startsWith('--'));
      const longBase = long ? long.split(' ')[0].replace(/^--/, '') : null;
      const shortBase = short ? short.split(' ')[0].replace(/^-/, '') : null;
      const takesArg = f.flag.includes('<');

      let line = `complete -c mgw -n '__fish_seen_subcommand_from ${cmd.name}'`;
      if (shortBase) line += ` -s ${shortBase}`;
      if (longBase)  line += ` -l ${longBase}`;
      line += ` -d '${f.desc}'`;
      if (takesArg)  line += ' -r';
      lines.push(line);
    }
  }

  // Commands with only global flags
  lines.push('');
  lines.push('# ---------------------------------------------------------------------------');
  lines.push('# Commands with only global flags');
  lines.push('# ---------------------------------------------------------------------------');
  const simpleCommands = COMMANDS.filter(c => c.flags.length === 0).map(c => c.name);
  lines.push(`for _mgw_cmd in ${simpleCommands.join(' ')}`);
  for (const f of GLOBAL_FLAGS) {
    const parts = f.flag.split(',').map(s => s.trim());
    const long = parts.find(p => p.startsWith('--'));
    const short = parts.find(p => p.startsWith('-') && !p.startsWith('--'));
    const longBase = long ? long.split(' ')[0].replace(/^--/, '') : null;
    const shortBase = short ? short.split(' ')[0].replace(/^-/, '') : null;
    const takesArg = f.flag.includes('<');

    let line = `  complete -c mgw -n "__fish_seen_subcommand_from $_mgw_cmd"`;
    if (shortBase) line += ` -s ${shortBase}`;
    if (longBase)  line += ` -l ${longBase}`;
    line += ` -d '${f.desc}'`;
    if (takesArg)  line += ' -r';
    lines.push(line);
  }
  lines.push('end');

  return lines.join('\n') + '\n';
}

// ---------------------------------------------------------------------------
// Main
// ---------------------------------------------------------------------------

function main() {
  const args = process.argv.slice(2);
  let outDir = path.join(__dirname, '..', 'completions');

  const outDirIdx = args.indexOf('--out-dir');
  if (outDirIdx !== -1 && args[outDirIdx + 1]) {
    outDir = path.resolve(args[outDirIdx + 1]);
  }

  fs.mkdirSync(outDir, { recursive: true });

  const files = {
    'mgw.bash': generateBash(),
    'mgw.zsh':  generateZsh(),
    'mgw.fish': generateFish(),
  };

  for (const [filename, content] of Object.entries(files)) {
    const dest = path.join(outDir, filename);
    fs.writeFileSync(dest, content, 'utf-8');
    console.log(`Written: ${dest}`);
  }

  console.log(`\nDone. ${Object.keys(files).length} completion scripts generated in ${outDir}`);
  console.log('\nInstallation:');
  console.log('  bash:  source completions/mgw.bash  (or copy to /etc/bash_completion.d/mgw)');
  console.log('  zsh:   Add completions/ to $fpath, then compinit');
  console.log('         Or: cp completions/mgw.zsh /usr/local/share/zsh/site-functions/_mgw');
  console.log('  fish:  cp completions/mgw.fish ~/.config/fish/completions/mgw.fish');
}

main();
