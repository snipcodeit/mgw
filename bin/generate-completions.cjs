/**
 * bin/generate-completions.cjs
 *
 * Generates shell completion scripts for mgw into the completions/ directory.
 * Reads subcommand and flag definitions from the same source-of-truth used by
 * bin/mgw.cjs so completions stay in sync when commands are added or changed.
 *
 * Usage:
 *   node bin/generate-completions.cjs [--out-dir <dir>]
 *
 * Default output: completions/ (relative to repo root)
 */

